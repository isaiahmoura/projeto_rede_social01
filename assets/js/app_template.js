function open_modal_user() {
    var open_modal_user = document.querySelector('.modal_user')

    open_modal_user.style.display = 'flex'
}
function sair_modal_user() {
    var open_modal_user = document.querySelector('.modal_user')

    open_modal_user.style.display = 'none'
}
function show_requests() {
    var requests = document.querySelector('.requests')

    requests.style.display = 'flex'
}